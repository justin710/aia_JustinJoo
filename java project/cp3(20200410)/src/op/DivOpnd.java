package op;

public class DivOpnd {

	public static void main(String[] args) {

		int num1 = 7;
		int num2 = 3;
		
		//정수의 나누기 연산
		System.out.println("num / num2 = "+num1/num2);
		System.out.println("num / num2 = "+num1/3f);
		
		//실수의 나누기 연산
		System.out.println("num / nun ="+ 7f/3f);
		System.out.println("num / nun ="+ (float)7/3);
	}

}
